          const isTopEdge = cy === nSY && tile.nativeY === 0;
          ctx.fillStyle = "rgba(0,0,0,0.45)";
          if (isRightEdge) ctx.fillRect(px + pw - 2, py, 2, ph);
          if (isLeftEdge) ctx.fillRect(px, py, 2, ph);
